def add_categories(category_list):
    for cat in sorted(category_list):
        slug = cat.lower().replace(' ', '_')
        try:
            PostCategory.objects.create(category_name=cat, slug_name=slug)
        except DatabaseError:
            pass


def add_countries(country_list):
    for c, code in country_list:
        try:
            Country.objects.create(name=c, phone_code=code)
        except DatabaseError:
            pass


def main(country_l, category_l):
    add_categories(category_l)
    add_countries(country_l)


